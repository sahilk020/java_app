	private final String name;
	private final String codeAlpha2;
	private final String codeAlpha3;
	
	private BinCountryMapperType(String name, String codeAlpha2, String codeAlpha3){
		this.name = name;
		this.codeAlpha2 = codeAlpha2;
		this.codeAlpha3 = codeAlpha3;
	}

	public String getName() {
		return name;
	}

	public String getCodeAlpha2(){
		return codeAlpha2;
	}

	public String getCodeAlpha3() {
		return codeAlpha3;
	}

	public static String getNameUsingCode(String code){
		return getInstance(code).getName();
	}
	public static BinCountryMapperType getInstance(String code){
		BinCountryMapperType[] binMapperTypes = BinCountryMapperType.values();
		for(BinCountryMapperType mapperType : binMapperTypes){
			if(mapperType.getCodeAlpha2().equalsIgnoreCase(code)){
				return mapperType;
			}
		}		
		return null;
	}

	public static String get3DigitCodeUsingName(String name){
		return getInstanceFromName(name).getCodeAlpha3();
	}

	public static BinCountryMapperType getInstanceFromName(String name){
		BinCountryMapperType[] binMapperTypes = BinCountryMapperType.values();
		for(BinCountryMapperType mapperType : binMapperTypes){
			if(mapperType.getName().equalsIgnoreCase(name)){
				return mapperType;
			}
		}		
		return null;
	}

