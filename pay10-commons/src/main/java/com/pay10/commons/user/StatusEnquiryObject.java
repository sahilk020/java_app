package com.pay10.commons.user;

public class StatusEnquiryObject {
	
	private String createDate;
	private String totalProcess;
	private String totalCapture;
	private String totalPending;
	private String totalOthers;
	

	public String getCreateDate() {
		return createDate;
	}
	public void setCreateDate(String createDate) {
		this.createDate = createDate;
	}
	public String getTotalProcess() {
		return totalProcess;
	}
	public void setTotalProcess(String totalProcess) {
		this.totalProcess = totalProcess;
	}
	public String getTotalCapture() {
		return totalCapture;
	}
	public void setTotalCapture(String totalCapture) {
		this.totalCapture = totalCapture;
	}
	public String getTotalPending() {
		return totalPending;
	}
	public void setTotalPending(String totalPending) {
		this.totalPending = totalPending;
	}
	public String getTotalOthers() {
		return totalOthers;
	}
	public void setTotalOthers(String totalOthers) {
		this.totalOthers = totalOthers;
	}
	
	
	
	

}
