package com.pay10.pg.action.dto;

public class TenureDetails {
    private String processflat;
    private String emiAmount;
    private String discount;
    private String roi;
    private String totalInterest;
    private String principalAmount;
    private String emiId;
    private String tenure;
    private String emiDesc;

    public String getProcessflat() {
        return processflat;
    }

    public void setProcessflat(String processflat) {
        this.processflat = processflat;
    }

    public String getEmiAmount() {
        return emiAmount;
    }

    public void setEmiAmount(String emiAmount) {
        this.emiAmount = emiAmount;
    }

    public String getDiscount() {
        return discount;
    }

    public void setDiscount(String discount) {
        this.discount = discount;
    }

    public String getRoi() {
        return roi;
    }

    public void setRoi(String roi) {
        this.roi = roi;
    }

    public String getTotalInterest() {
        return totalInterest;
    }

    public void setTotalInterest(String totalInterest) {
        this.totalInterest = totalInterest;
    }

    public String getPrincipalAmount() {
        return principalAmount;
    }

    public void setPrincipalAmount(String principalAmount) {
        this.principalAmount = principalAmount;
    }

    public String getEmiId() {
        return emiId;
    }

    public void setEmiId(String emiId) {
        this.emiId = emiId;
    }

    public String getTenure() {
        return tenure;
    }

    public void setTenure(String tenure) {
        this.tenure = tenure;
    }

    public String getEmiDesc() {
        return emiDesc;
    }

    public void setEmiDesc(String emiDesc) {
        this.emiDesc = emiDesc;
    }
}
