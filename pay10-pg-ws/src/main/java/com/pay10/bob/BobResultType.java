	public static BobResultType getInstanceFromName(String code) {
		BobResultType[] statusTypes = BobResultType.values();
		for (BobResultType statusType : statusTypes) {
			if (String.valueOf(statusType.getBankCode()).toUpperCase().equals(code)) {
				return statusType;
			}
		}
		return null;
	}

